# Operator Precedence

=begin
    Ruby evaluates a mathematical expression using an
    order of operations that is based on operator precedence. 
    Exponentiation has the highest precedence followed by 
    multiplication, division, and modulus from left to right, and 
    then addition and subtraction from left to right.
    You can change the order of operations by using parentheses.
=end

